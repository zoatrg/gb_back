  function showLegalModal(type) {
    if (document.getElementById('legalModal')) return;

    const overlay = document.createElement('div');
    overlay.id = 'legalModal';
    overlay.className = 'legal-modal';

    const html = type === 'terms' ? buildTermsHTML() : buildPrivacyHTML();

    overlay.innerHTML =
      '<div class="legal-modal__backdrop"></div>' +
      '<div class="legal-modal__dialog">' +
        '<button class="legal-modal__close-btn" aria-label="닫기">' +
          '<svg viewBox="0 0 24 24"><path d="M15.18 12l7.16-7.16a2.25 2.25 0 10-3.18-3.18L12 8.82 4.84 1.66a2.25 2.25 0 10-3.18 3.18L8.82 12l-7.16 7.16a2.25 2.25 0 103.18 3.18L12 15.18l7.16 7.16a2.25 2.25 0 103.18-3.18z" fill="#111"/></svg>' +
        '</button>' +
        html +
      '</div>';

    document.body.appendChild(overlay);
    requestAnimationFrame(function () { overlay.classList.add('legal-modal--open'); });

    overlay.querySelector('.legal-modal__backdrop').addEventListener('click', closeLegalModal);
    overlay.querySelector('.legal-modal__close-btn').addEventListener('click', closeLegalModal);

    overlay._escHandler = function (e) {
      if (e.key === 'Escape') closeLegalModal();
    };
    document.addEventListener('keydown', overlay._escHandler);
  }

  function closeLegalModal() {
    const overlay = document.getElementById('legalModal');
    if (!overlay) return;

    overlay.classList.remove('legal-modal--open');
    if (overlay._escHandler) {
      document.removeEventListener('keydown', overlay._escHandler);
    }
    setTimeout(function () { overlay.remove(); }, 250);
  }

